/*
* @fn        vdg_DMT_Set_Register(void) 
* @brief     DMTレジスタを初期化します
* @param
* @return
* @detail
*/
/*----------------------------------------------------------------------------*/
void vdg_DMT_Set_Register(void)
{
#if ( PMD_DMTMD == PMD_MODULE_ENABLE )		
	/*-----------------------------------------------------------------------*/
	/* DMTCONレジスタ */
	/*-----------------------------------------------------------------------*/
		DMTCON = 0x00000000u;
		#ifdef DEF_BITNAME
			DMTCONbits.ON = 0u;
		#endif 

	/*-----------------------------------------------------------------------*/
	/* DMTPRECLRレジスタ */
	/*-----------------------------------------------------------------------*/
		DMTPRECLR = 0x00000000u;
		#ifdef DEF_BITNAME
			DMTPRECLRbits.STEP1 = 0u;
		#endif 

	/*-----------------------------------------------------------------------*/
	/* DMTCLRレジスタ */
	/*-----------------------------------------------------------------------*/
		DMTCLR = 0x00000000u;
		#ifdef DEF_BITNAME
			DMTCLRbits.STEP2 = 0u;
		#endif 

	/*-----------------------------------------------------------------------*/
	/* DMTSTATレジスタ */
	/*-----------------------------------------------------------------------*/
		DMTSTAT = 0x00000000u;
		#ifdef DEF_BITNAME
			DMTSTATbits.WINOPN = 0u;
			DMTSTATbits.EVENT = 0u;
			DMTSTATbits.BAD2 = 0u;
			DMTSTATbits.BAD1 = 0u;
		#endif 

	/*-----------------------------------------------------------------------*/
	/* DMTCNTレジスタ */
	/*-----------------------------------------------------------------------*/
        DMTCNT = 0x00000000u;

#endif
}
