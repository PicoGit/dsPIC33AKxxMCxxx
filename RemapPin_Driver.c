/*
* @fn        vdg_RemapPin_Set_Input_Register(void) 
* @brief        リマッパブル入力ピンのレジスタをセットします 
* @param
* @return
* @detail
*/
/*----------------------------------------------------------------------------*/
void vdg_RemapPin_Set_Input_Register(void)
{
	/*-----------------------------------------------------------------------*/
	/* RPCONレジスタ */
	/*-----------------------------------------------------------------------*/
		RPCON = 0x00000000u;
		#ifdef DEF_BITNAME
			RPCONbits.IOLOCK = 0u;
		#endif 


    /*-----------------------------------------------------------------------*/
	/* RPINR0レジスタ */
	/*-----------------------------------------------------------------------*/     
        RPINR0	= ( RPI_USER_INT1 << 8u) |		/* INT1入力ピン選択ビット */
				  ( RPI_USER_INT2 << 16u) |     /* INT2入力ピン選択ビット */
                  ( RPI_USER_INT3 << 24u);      /* INT3入力ピン選択ビット */
    /*-----------------------------------------------------------------------*/
	/* RPINR1レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR1 = ( RPI_USER_INT4 << 0u) |		/* INT4入力ピン選択ビット */
				 ( RPI_USER_T1CK << 8u) |		/* T1CK入力ピン選択ビット */
				 ( RPI_USER_REFI1 << 16u) |     /* REFI1入力ピン選択ビット */
                 ( RPI_USER_REFI2 << 24u);      /* REFI2入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR2レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR2 = ( RPI_USER_ICM1 << 0u) |		/* ICM1入力ピン選択ビット */
				 ( RPI_USER_ICM2 << 8u) |		/* ICM2入力ピン選択ビット */
				 ( RPI_USER_ICM3 << 16u) |      /* ICM3入力ピン選択ビット */
                 ( RPI_USER_ICM4 << 24u);       /* ICM4入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR5レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR5 = ( RPI_USER_OCFA << 0u) |		/* OCFA入力ピン選択ビット */
				 ( RPI_USER_OCFB << 8u) |		/* OCFB入力ピン選択ビット */
				 ( RPI_USER_OCFC << 16u) |      /* OCFC入力ピン選択ビット */
                 ( RPI_USER_OCFD << 24u);       /* OCFD入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR6レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR6 = ( RPI_USER_PCI8 << 0u) |		/* PCI8入力ピン選択ビット */
				 ( RPI_USER_PCI9 << 8u) |		/* PCI9入力ピン選択ビット */
				 ( RPI_USER_PCI10 << 16u) |     /* PCI10入力ピン選択ビット */
                 ( RPI_USER_PCI11 << 24u);      /* PCI11入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR7レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR7 = ( RPI_USER_QEIA1 << 0u) |		/* QEIA1入力ピン選択ビット */
				 ( RPI_USER_QEIB1 << 8u) |		/* QEIB1入力ピン選択ビット */
				 ( RPI_USER_QEIINDX1 << 16u) |     /* QEIINDX1入力ピン選択ビット */
                 ( RPI_USER_QEIHOME1 << 24u);      /* QEIHOME1入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR9レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR9 = ( RPI_USER_U1RX << 0u) |		/* U1RX入力ピン選択ビット */
				 ( RPI_USER_U1DSR << 8u) |		/* U1DSR入力ピン選択ビット */
				 ( RPI_USER_U2RX << 16u) |      /* U2RX1入力ピン選択ビット */
                 ( RPI_USER_U2DSR << 24u);      /* U2DSR入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR10レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR10 = ( RPI_USER_SDI1 << 0u) |		/* SDI1入力ピン選択ビット */
				 ( RPI_USER_SCK1 << 8u) |		/* SCK1入力ピン選択ビット */
				 ( RPI_USER_SS1 << 16u) ;       /* SS1入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR11レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR11 = ( RPI_USER_SDI2 << 0u) |		/* SDI2入力ピン選択ビット */
				 ( RPI_USER_SCK2 << 8u) |		/* SCK2入力ピン選択ビット */
				 ( RPI_USER_SS2 << 16u) ;       /* SS2入力ピン選択ビット */
		

	/*-----------------------------------------------------------------------*/
	/* RPINR13レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR13 = ( RPI_USER_U3RX << 0u) |		/* U3RX入力ピン選択ビット */
				 ( RPI_USER_U3DSR << 8u) ;		/* U3DSR入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR14レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR14 = ( RPI_USER_SENT1 << 16u) |     /* SENT1入力ピン選択ビット */
                 ( RPI_USER_SENT2 << 24u);      /* SENT2入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR15レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR15 = ( RPI_USER_SDI3 << 0u) |		/* SDI3入力ピン選択ビット */
				 ( RPI_USER_SCK3 << 8u) |		/* SCK3入力ピン選択ビット */
				 ( RPI_USER_SS3 << 16u) ;       /* SS3入力ピン選択ビット */
	/*-----------------------------------------------------------------------*/
	/* RPINR17レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR17 = ( RPI_USER_PCI12 << 0u) |		/* PCI12入力ピン選択ビット */
				 ( RPI_USER_PCI13 << 8u) |		/* PCI13入力ピン選択ビット */
				 ( RPI_USER_PCI14 << 16u) |     /* PCI14入力ピン選択ビット */
				 ( RPI_USER_PCI15 << 24u);      /* PCI15入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR18レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR18 = ( RPI_USER_PCI16 << 0u) |		/* PCI16入力ピン選択ビット */
				 ( RPI_USER_PCI17 << 8u) |		/* PCI17入力ピン選択ビット */
				 ( RPI_USER_PCI18 << 16u) |     /* PCI18入力ピン選択ビット */
				 ( RPI_USER_ADTRIG31 << 24u);   /* ADTRIG31入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR19レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR19 = ( RPI_USER_BISS1SL << 0u) |    /* BISS1SL入力ピン選択ビット */
                 ( RPI_USER_BISS1GS << 8u);      /* SENT2入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR20レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR20 = ( RPI_USER_CLCINA << 0u) |	/* CLCINA入力ピン選択ビット */
				 ( RPI_USER_CLCINB << 8u) |		/* CLCINB入力ピン選択ビット */
				 ( RPI_USER_CLCINC << 16u) |    /* CLCINC入力ピン選択ビット */
				 ( RPI_USER_CLCIND << 24u);     /* CLCIND入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPINR21レジスタ */
	/*-----------------------------------------------------------------------*/
		RPINR21 =  ( RPI_USER_U1CTS << 0u) |	/* U1CTS入力ピン選択ビット */
				 ( RPI_USER_U2CTS << 8u) |		/* U2CTS入力ピン選択ビット */
				 ( RPI_USER_U3CTS << 16u) ;    /* U3CTS入力ピン選択ビット */

	/*-----------------------------------------------------------------------*/
	/* RPOR0レジスタ */
	/*-----------------------------------------------------------------------*/
		RPOR0 = ( RPO_USER_RP1R << 0u) |		/* RP1出力ピン選択ビット */
				 ( RPO_USER_RP2R << 8u) |		/* RP2出力ピン選択ビット */
				 ( RPO_USER_RP3R << 16u) |      /* RP3出力ピン選択ビット */
				 ( RPO_USER_RP4R << 24u);       /* RP4出力ピン選択ビット */

	
}
