/*
* @fn        vdg_ECC_Set_Register(void)
* @brief        ECCレジスタを初期化します
* @param
* @return
* @detail 
*/
/*----------------------------------------------------------------------------*/
void vdg_ECC_Set_Register(void)
{
#if ( PMD_ECCMD == PMD_MODULE_ENABLE )
    /*----------------------------------------------------------------------------*/
    /* ECCフォルト注入設定レジスタL */
    /*----------------------------------------------------------------------------*/
        ECCCONL	= 0x0000u;	
#if 0   /*コード量削減の為、ビット毎のクリアを無効化にする*/
            ECCCONLbits.FLTINJ	= 0u;	/* フォルト注入シーケンス有効化ビット */
#endif /* コード量削減 */
    /*----------------------------------------------------------------------------*/        
    /* ECCフォルト注入設定レジスタH */
    /*----------------------------------------------------------------------------*/
        ECCCONH	= 0x0000u;	
#if 0   /*コード量削減の為、ビット毎のクリアを無効化にする*/
            ECCCONHbits.FLT1PTR	= 0u;	/* フォルト注入ポインタ1 */
            ECCCONHbits.FTL2PTR	= 0u;	/* フォルト注入ポインタ2 */
#endif /* コード量削減 */
    /*----------------------------------------------------------------------------*/
    /* ECCフォルト注入アドレス比較レジスタL */
    /*----------------------------------------------------------------------------*/
        ECCADDRL	= 0x0000u;	
    /*----------------------------------------------------------------------------*/
    /* ECCフォルト注入アドレス比較レジスタH */
    /*----------------------------------------------------------------------------*/
        ECCADDRH	= 0x0000u;	
    /*----------------------------------------------------------------------------*/
    /* ECCシステムステータス表示レジスタ */
    /*----------------------------------------------------------------------------*/
        ECCSTATL	= 0x0000u;	
#if 0   /*コード量削減の為、ビット毎のクリアを無効化にする*/
            ECCSTATLbits.SECIN	= 0u;	/* 読み込み単一誤り訂正パリティ値ビット */
            ECCSTATLbits.SECOUT	= 0u;	/* 計算された単一エラー訂正パリティ値ビット */
#endif /* コード量削減 */
    /*----------------------------------------------------------------------------*/
    /* レジスタ説明 */
    /*----------------------------------------------------------------------------*/
        ECCSTATH	= 0x0000u;	
#if 0   /*コード量削減の為、ビット毎のクリアを無効化にする*/
            ECCSTATHbits.SYND	= 0u;	/* 計算されたECCシンドローム値ビット */
            ECCSTATHbits.DEDIN	= 0u;	/* デュアルビットエラー検出パリティビットを読む */
            ECCSTATHbits.DEDOUT	= 0u;	/* 計算されたデュアルビットエラー検出パリティビット */
#endif /* コード量削減 */
#endif
}
